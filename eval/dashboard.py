class DashboardBuilder:
    """Builds an HTML dashboard from evaluation report data."""

    def __init__(self, title: str = "RAG Evaluation Dashboard"):
        self._title = title
        self._sections: List[str] = []

    def _escape(self, text: str) -> str:
        return escape(str(text))

    def _timestamp(self) -> str:
        return datetime.now(timezone.utc).strftime("%Y-%m-%d %H:%M:%S UTC")

    def add_summary_cards(self, summary: Dict[str, Any]) -> "DashboardBuilder":
        """Add summary metric cards at the top of the dashboard."""
        total = summary.get("total_cases", 0)
        passed = summary.get("passed_cases", 0)
        failed = summary.get("failed_cases", 0)
        pass_rate = summary.get("pass_rate", 0)
        elapsed = summary.get("elapsed_seconds", 0)

        html = f"""
        <div class="section">
          <h2>Summary</h2>
          <p class="timestamp">{self._escape(summary.get('timestamp', ''))}</p>
          <div class="summary-grid">
            <div class="summary-card">
              <div class="value">{total}</div>
              <div class="label">Total Cases</div>
            </div>
            <div class="summary-card">
              <div class="value pass">{passed}</div>
              <div class="label">Passed</div>
            </div>
            <div class="summary-card">
              <div class="value fail">{failed}</div>
              <div class="label">Failed</div>
            </div>
            <div class="summary-card">
              <div class="value {'pass' if pass_rate >= 0.95 else 'fail'}">{pass_rate:.1%}</div>
              <div class="label">Pass Rate</div>
            </div>
            <div class="summary-card">
              <div class="value">{summary.get('projects_evaluated', 0)}</div>
              <div class="label">Projects</div>
            </div>
            <div class="summary-card">
              <div class="value">{elapsed:.1f}s</div>
              <div class="label">Duration</div>
            </div>
          </div>
        </div>
        """
        self._sections.append(html)
        return self

    def add_custom_metrics(self, custom_metrics: Dict[str, Any]) -> "DashboardBuilder":
        """Add Spec 3.5 custom metrics section."""
        overall = custom_metrics.get("overall", {})
        required = custom_metrics.get("required_thresholds", {})
        all_passed = custom_metrics.get("all_metrics_passed", False)

        metrics_rows = ""
        for key in ["golden_set_pass_rate", "answer_stability", "regulatory_compliance",
                     "role_appropriateness", "hitl_trigger_precision"]:
            value = overall.get(key, 0)
            req = required.get(key, 0)
            passed = value >= req
            badge_class = "badge-pass" if passed else "badge-fail"
            bar_color = "#16a34a" if passed else "#dc2626"
            bar_width = min(100, int(value * 100))
            metrics_rows += f"""
            <tr>
              <td>{key.replace('_', ' ').title()}</td>
              <td>{value:.2%}</td>
              <td>{req:.0%}</td>
              <td><span class="badge {badge_class}">{'PASS' if passed else 'FAIL'}</span></td>
              <td>
                <div class="metric-bar">
                  <div class="metric-bar-fill" style="width: {bar_width}%; background: {bar_color};"></div>
                </div>
              </td>
            </tr>
            """

        status_class = "alert-success" if all_passed else "alert-danger"
        status_text = "✅ All Metrics Passed" if all_passed else "❌ Some Metrics Failed"

        html = f"""
        <div class="section">
          <h2>Custom Metrics (Spec 3.5)</h2>
          <div class="alert {status_class}">{status_text}</div>
          <table>
            <thead>
              <tr>
                <th>Metric</th>
                <th>Value</th>
                <th>Required</th>
                <th>Status</th>
                <th>Progress</th>
              </tr>
            </thead>
            <tbody>
              {metrics_rows}
            </tbody>
          </table>
        </div>
        """
        self._sections.append(html)
        return self

    def add_results_table(self, results: List[Dict[str, Any]]) -> "DashboardBuilder":
        """Add per-case results table."""
        if not results:
            return self

        rows = ""
        for r in results:
            case_id = self._escape(r.get("id", ""))
            query = self._escape(r.get("query", "")[:80])
            difficulty = self._escape(r.get("difficulty", ""))
            project = self._escape(r.get("project", ""))
            passed = r.get("passed", False)
            judge_score = r.get("judge", {}).get("overall_score", 0)

            badge_class = "badge-pass" if passed else "badge-fail"
            badge_text = "PASS" if passed else "FAIL"
            judge_color = "pass" if judge_score >= 0.8 else ("warn" if judge_score >= 0.6 else "fail")

            rows += f"""
            <tr>
              <td>{case_id}</td>
              <td title="{self._escape(r.get('query', ''))}">{query}</td>
              <td><span class="badge badge-{'pass' if difficulty == 'easy' else ('warn' if difficulty == 'medium' else 'fail')}">{difficulty}</span></td>
              <td>{project}</td>
              <td><span class="badge {badge_class}">{badge_text}</span></td>
              <td class="{judge_color}">{judge_score:.3f}</td>
            </tr>
            """

        html = f"""
        <div class="section">
          <h2>Per-Case Results ({len(results)} cases)</h2>
          <table>
            <thead>
              <tr>
                <th>ID</th>
                <th>Query</th>
                <th>Difficulty</th>
                <th>Project</th>
                <th>Status</th>
                <th>Judge Score</th>
              </tr>
            </thead>
            <tbody>
              {rows}
            </tbody>
          </table>
        </div>
        """
        self._sections.append(html)
        return self

    def add_failure_analysis(self, summary: Dict[str, Any]) -> "DashboardBuilder":
        """Add failure analysis section."""
        failure_buckets = summary.get("failure_buckets", {})
        if not any(failure_buckets.values()):
            return self

        sections_html = ""
        for bucket, cases in failure_buckets.items():
            if cases:
                bucket_label = bucket.replace("_", " ").title()
                case_list = "".join(
                    f"<li>{self._escape(c)}</li>" for c in cases[:20]
                )
                sections_html += f"""
                <div>
                  <h3>{bucket_label} ({len(cases)})</h3>
                  <ul>{case_list}</ul>
                </div>
                """

        html = f"""
        <div class="section">
          <h2>Failure Analysis</h2>
          {sections_html}
        </div>
        """
        self._sections.append(html)
        return self

    def add_baseline_comparison(self, comparison: Dict[str, Any]) -> "DashboardBuilder":
        """Add baseline comparison section."""
        if not comparison or "error" in comparison:
            return self

        delta = comparison.get("pass_rate_delta", 0)
        delta_class = "pass" if delta >= 0 else "fail"
        delta_sign = "+" if delta >= 0 else ""

        regressions = comparison.get("regressions", [])
        improvements = comparison.get("improvements", [])

        regression_rows = ""
        for reg in regressions[:10]:
            bj = reg.get("baseline_judge", "N/A")
            cj = reg.get("current_judge", "N/A")
            regression_rows += f"""
            <tr>
              <td>{self._escape(reg.get('id', ''))}</td>
              <td>{self._escape(reg.get('query', '')[:60])}</td>
              <td>{bj}</td>
              <td class="fail">{cj}</td>
            </tr>
            """

        improvement_rows = ""
        for imp in improvements[:10]:
            improvement_rows += f"""
            <tr>
              <td>{self._escape(imp.get('id', ''))}</td>
              <td>{self._escape(imp.get('query', '')[:60])}</td>
              <td class="pass">PASS</td>
            </tr>
            """

        html = f"""
        <div class="section">
          <h2>Baseline Comparison</h2>
          <div class="summary-grid">
            <div class="summary-card">
              <div class="value {delta_class}">{delta_sign}{delta:.2%}</div>
              <div class="label">Pass Rate Delta</div>
            </div>
            <div class="summary-card">
              <div class="value fail">{comparison.get('regression_count', 0)}</div>
              <div class="label">Regressions</div>
            </div>
            <div class="summary-card">
              <div class="value pass">{comparison.get('improvement_count', 0)}</div>
              <div class="label">Improvements</div>
            </div>
            <div class="summary-card">
              <div class="value">{len(comparison.get('new_cases', []))}</div>
              <div class="label">New Cases</div>
            </div>
          </div>
        """

        if regression_rows:
            html += f"""
          <h3>Regressions</h3>
          <table>
            <thead><tr><th>ID</th><th>Query</th><th>Baseline</th><th>Current</th></tr></thead>
            <tbody>{regression_rows}</tbody>
          </table>
            """

        if improvement_rows:
            html += f"""
          <h3>Improvements</h3>
          <table>
            <thead><tr><th>ID</th><th>Query</th><th>Status</th></tr></thead>
            <tbody>{improvement_rows}</tbody>
          </table>
            """

        html += "</div>"
        self._sections.append(html)
        return self

    def add_drift_alerts(self, drift_data: Optional[Dict[str, Any]] = None) -> "DashboardBuilder":
        """Add drift detection alerts section."""
        if not drift_data:
            return self

        alerts = drift_data.get("alerts", [])
        drift_score = drift_data.get("drift_score", 0)

        if drift_score < 0.1:
            level = "none"
            level_label = "No Drift Detected"
        elif drift_score < 0.3:
            level = "minor"
            level_label = "Minor Drift Detected"
        else:
            level = "major"
            level_label = "Major Drift Detected"

        alert_class = "alert-success" if level == "none" else ("alert-info" if level == "minor" else "alert-danger")

        alerts_html = ""
        for alert in alerts[:10]:
            severity = alert.get("severity", "info")
            sev_class = "alert-danger" if severity == "critical" else ("alert-info" if severity == "warning" else "alert-info")
            alerts_html += f"""
            <div class="alert {sev_class}">
              <strong>{self._escape(alert.get('metric', ''))}</strong><br>
              {self._escape(alert.get('message', ''))}
            </div>
            """

        html = f"""
        <div class="section">
          <h2>Drift Detection</h2>
          <div class="alert {alert_class}">
            <span class="drift-indicator drift-{level}"></span>
            <strong>{level_label}</strong> (score: {drift_score:.4f})
          </div>
          {alerts_html}
        </div>
        """
        self._sections.append(html)
        return self

    def add_ab_comparison(self, ab_data: Optional[Dict[str, Any]] = None) -> "DashboardBuilder":
        """Add A/B comparison results section."""
        if not ab_data:
            return self

        a_label = ab_data.get("labels", {}).get("a", "A")
        b_label = ab_data.get("labels", {}).get("b", "B")
        winner = ab_data.get("overall_winner", "tie")
        winner_label = ab_data.get("winner_label", "No significant difference")
        sig = ab_data.get("significance", {})

        results = ab_data.get("results", {})
        scores = ab_data.get("scores", {})

        html = f"""
        <div class="section">
          <h2>A/B Comparison: {self._escape(a_label)} vs {self._escape(b_label)}</h2>
          <div class="alert alert-info">
            <strong>Winner: {self._escape(winner_label)}</strong>
            (p-value: {sig.get('p_value', 'N/A')}, {'significant' if sig.get('significant') else 'not significant'})
          </div>
          <div class="summary-grid">
            <div class="summary-card">
              <div class="value">{results.get('a_wins', 0)}</div>
              <div class="label">{self._escape(a_label)} Wins</div>
            </div>
            <div class="summary-card">
              <div class="value">{results.get('b_wins', 0)}</div>
              <div class="label">{self._escape(b_label)} Wins</div>
            </div>
            <div class="summary-card">
              <div class="value">{results.get('ties', 0)}</div>
              <div class="label">Ties</div>
            </div>
            <div class="summary-card">
              <div class="value">{scores.get('a_mean', 0):.3f}</div>
              <div class="label">Mean {self._escape(a_label)}</div>
            </div>
            <div class="summary-card">
              <div class="value">{scores.get('b_mean', 0):.3f}</div>
              <div class="label">Mean {self._escape(b_label)}</div>
            </div>
          </div>
        </div>
        """
        self._sections.append(html)
        return self

    def add_raw_data_section(self, data: Dict[str, Any], label: str = "Raw Report Data") -> "DashboardBuilder":
        """Add a collapsible raw data section for debugging."""
        json_str = json.dumps(data, indent=2, default=str)[:5000]
        html = f"""
        <div class="section">
          <h2>{self._escape(label)}</h2>
          <details>
            <summary>Click to expand JSON</summary>
            <pre>{self._escape(json_str)}</pre>
          </details>
        </div>
        """
        self._sections.append(html)
        return self

    def build(self) -> str:
        """Assemble the final HTML page."""
        css = _CSS_STYLES
        header = _HEADER.format(title=self._escape(self._title), css=css)
        body = "\n".join(self._sections)
        footer = _FOOTER.format(timestamp=self._timestamp())
        return header + body + footer


# ---------------------------------------------------------------------------
# Build dashboard from report
# ---------------------------------------------------------------------------

def build_dashboard(
    report: Dict[str, Any],
    output_path: Optional[str] = None,
    drift_data: Optional[Dict[str, Any]] = None,
    ab_data: Optional[Dict[str, Any]] = None,
    title: str = "RAG Evaluation Dashboard",
) -> str:
    """Build a complete HTML dashboard from an evaluation report.

    Args:
        report: The evaluation report dict from run_regression().
        output_path: Optional file path to write the HTML.
        drift_data: Optional drift detection data.
        ab_data: Optional A/B comparison data.
        title: Dashboard page title.

    Returns:
        The HTML string.
    """
    builder = DashboardBuilder(title=title)

    summary = report.get("summary", {})
    results = report.get("results", [])
    custom_metrics = report.get("custom_metrics")
    comparison = report.get("comparison")

    builder.add_summary_cards(summary)

    if custom_metrics:
        builder.add_custom_metrics(custom_metrics)

    if drift_data:
        builder.add_drift_alerts(drift_data)

    if ab_data:
        builder.add_ab_comparison(ab_data)

    if comparison:
        builder.add_baseline_comparison(comparison)

    if results:
        builder.add_results_table(results)
        builder.add_failure_analysis(summary)

    builder.add_raw_data_section(report.get("summary", {}), label="Summary (Machine Readable)")

    html = builder.build()

    if output_path:
        Path(output_path).write_text(html, encoding="utf-8")

    return html


# ---------------------------------------------------------------------------
# CLI entry point
# ---------------------------------------------------------------------------

def main() -> None:
    """CLI entry point for generating dashboards."""
    import argparse

    parser = argparse.ArgumentParser(
        description="Generate RAG evaluation HTML dashboard."
    )
    parser.add_argument(
        "--input", "-i",
        required=True,
        help="Path to evaluation report JSON file.",
    )
    parser.add_argument(
        "--output", "-o",
        default="dashboard.html",
        help="Output HTML file path (default: dashboard.html).",
    )
    parser.add_argument(
        "--drift",
        default=None,
        help="Path to drift detection data JSON file.",
    )
    parser.add_argument(
        "--ab",
        default=None,
        help="Path to A/B comparison data JSON file.",
    )
    parser.add_argument(
        "--title",
        default="RAG Evaluation Dashboard",
        help="Dashboard page title.",
    )

    args = parser.parse_args()

    with open(args.input, "r", encoding="utf-8") as f:
        report = json.load(f)

    drift_data = None
    if args.drift:
        with open(args.drift, "r", encoding="utf-8") as f:
            drift_data = json.load(f)

    ab_data = None
    if args.ab:
        with open(args.ab, "r", encoding="utf-8") as f:
            ab_data = json.load(f)

    html = build_dashboard(
        report=report,
        output_path=args.output,
        drift_data=drift_data,
        ab_data=ab_data,
        title=args.title,
    )

    print(f"Dashboard written to {args.output}")
    print(f"  Size: {len(html)} bytes")


if __name__ == "__main__":
    main()